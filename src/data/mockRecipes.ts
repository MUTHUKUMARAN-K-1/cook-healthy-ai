// Helper function to get recipe by ID
export function getRecipeById(id: string) {
  return mockRecipes.find(recipe => recipe.id === id);
}

// Helper function to get recipes by diet type
export function getRecipesByDiet(diet: string) {
  return mockRecipes.filter(recipe => {
    if (Array.isArray(recipe.dietType)) {
      return recipe.dietType.some(d => d.toLowerCase().includes(diet.toLowerCase()));
    }
    return recipe.dietType.toLowerCase().includes(diet.toLowerCase());
  });
}

// Helper function to get budget-friendly recipes
export function getBudgetRecipes(maxCost: number) {
  return mockRecipes.filter(recipe => recipe.costPerServing <= maxCost);
}

